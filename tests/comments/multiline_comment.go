package main

func main() {
	/*
	Это многострочный комментарий
	который занимает несколько строк.
	*/
	x := 10

	/*
	Это многострочный комментарий.

	Содержит пустую строку между строками комментария.
	*/
	y := 20

	/*
	Первый комментарий.
	// Это вложенный комментарий
	Который тоже будет проигнорирован.
	*/
	z := 30

	f := 10 /* Это комментарий на той же строке */

	{
		// Аналог /*коммент*/ f /*...*/ = /*...*/ 10" c комментариями между элементами:
		var /* Это комментарий между элементами */ f /*который*/ = /* не влияет на выражение */ 10
		_ = f // используем, чтобы не было предупреждения о неиспользуемой переменной
	}

	/*
	Комментарий, содержащий символы
	/
	/
	Которые в некоторых наивных парсерах могут "ломать" разбор,
	но для корректного лексера/Go-комментариев это обычный текст.
	*/

	// Используем переменные, чтобы избежать предупреждений компилятора.
	_ = x + y + z + f
}
