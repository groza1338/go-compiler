package main

func main() {
	a := 2 + 3 * 4          // умножение раньше сложения
	b := (2 + 3) * 4        // скобки меняют приоритет
	c := 10 - 2*3 + 1       // слева направо на одном уровне
	d := 10 - (2*3 + 1)     // скобки внутри
	e := 8 / 2 / 2          // деление слева направо
	f := 8 / (2 / 2)        // скобки меняют порядок деления
	g := 2 + 3 * (4 + 5)    // вложенные скобки с разными приоритетами
	h := (2 + 3) * (4 + 5)  // скобки вокруг обеих операций

	logical := (a > b) && (c < d || e == f) // логические операторы после сравнений
	mixed := a + b*2 - c/2                  // смешанные арифметические операции

	_ = a
	_ = b
	_ = c
	_ = d
	_ = e
	_ = f
	_ = g
	_ = h
	_ = logical
	_ = mixed
}
